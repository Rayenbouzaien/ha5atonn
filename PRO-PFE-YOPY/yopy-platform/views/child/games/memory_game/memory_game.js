        /* ─── FLIP ─── */
        let firstFlipTime = null;

        function flip(card, val) {
            if (lock || card.classList.contains('flipped') || card.classList.contains('matched')) return;
            card.classList.add('flipped');

            // First card of a pair — just record the timestamp
            if (!first) {
                first         = { card, val };
                firstFlipTime = Date.now();
                return;
            }

            // Second card → measure reaction time for this pair attempt
            const reactionMs = Date.now() - firstFlipTime;

            if (first.val === val) {
                // ── MATCH ──
                // reaction: how long between card 1 and card 2 (decision speed)
                // success:  value = running pair count (streak proxy for Python model)
                const c = getCollector();
                if (c) {
                    c.record('card_flip', reactionMs);  // → 'reaction'
                    c.record('match',     pairs + 1);   // → 'success'
                }

                card.classList.add('matched');
                first.card.classList.add('matched');
                pairs++;

                gsap.from([card, first.card], { scale: 1.15, duration: 0.4, ease: 'back.out(2)', stagger: 0.05 });
                flashCombo();
                spawnMatchParticles(card);
                updateHUD();
                updateProgress();

                first         = null;
                firstFlipTime = null;

            } else {
                // ── MISMATCH ──
                // reaction: how long before making the wrong choice
                // error:    the mistake itself (null value — presence is the signal)
                const c = getCollector();
                if (c) {
                    c.record('card_flip',  reactionMs);  // → 'reaction'
                    c.record('mismatch',   null);        // → 'error'
                }

                errors++;
                card.classList.add('wrong');
                first.card.classList.add('wrong');
                lock = true;

                gsap.to([card, first.card], {
                    x: [-6, 6, -4, 4, 0],
                    duration: 0.4,
                    ease: 'none',
                    keyframes: true,
                    onComplete: () => {
                        const prev = first;
                        setTimeout(() => {
                            card.classList.remove('flipped', 'wrong');
                            prev.card.classList.remove('flipped', 'wrong');
                            first         = null;
                            firstFlipTime = null;
                            lock          = false;
                        }, 300);
                    }
                });
                updateHUD();
            }
        }


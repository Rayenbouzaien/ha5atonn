async function botMoveAsync() {
    if (roundOver) return;
    let idx = pickBotMove();
    placeSymbol(idx, 'O');
    if (collector) collector.record('bot_move', null);

    let res = checkResult();
    if (res) {
        await handleRoundEnd(res);
        return;
    }
    playerTurn = true;
    unlockBoard();
    setTurnMsg('✍️ Your move');
}
async function playerMove(idx) {
    if (!playerTurn || roundOver || gameState !== STATE.PLAYING || board[idx] !== null) return;

    placeSymbol(idx, 'X');
    if (collector) collector.record('player_move', null);

    let result = checkResult();
    if (result) {
        await handleRoundEnd(result);
        return;
    }

    playerTurn = false;
    lockBoard(true);
    setTurnMsg('🍃 AI thinking...');
    setTimeout(() => botMoveAsync(), DIFF_CONFIG[difficulty].botDelay);
}

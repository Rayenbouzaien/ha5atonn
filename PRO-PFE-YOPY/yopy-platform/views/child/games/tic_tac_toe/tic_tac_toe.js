   async function handleRoundEnd(result) {
    roundOver = true;
    lockBoard(true);
    if (result.winner === 'X') {
        highlightWin(result.line, 'X');
        scoreYou++;
        sessionScore += DIFF_CONFIG[difficulty].mult;
        if (collector) collector.record('win', null);
        setTurnMsg('✨ Brilliant! You win ✨');
    } else if (result.winner === 'O') {
        highlightWin(result.line, 'O');
        scoreBot++;
        if (collector) collector.record('lose', null);
        setTurnMsg('🍂 The AI wins...');
    } else {
        if (difficulty === 'hard') sessionScore += Math.floor(DIFF_CONFIG.hard.mult / 2);
        scoreTie++;
        if (collector) collector.record('tie', null);
        setTurnMsg('🤝 Tie · Balanced mind');
    }
    updateScores();
    round++;
    showRoundResultPopup(result);
}

async function endSession() {
    if (gameState !== STATE.PLAYING) return;
    gameState = STATE.DONE;

    if (collector) {
        await collector.flush();
    }

    let elapsed = gameStartTime ? Math.max(3, Math.floor((Date.now() - gameStartTime) / 1000)) : 3;

    let emoji, title;
    if (scoreYou > scoreBot) { emoji = '🏆 Victory'; title = 'Masterful Stroke'; }
    else if (scoreBot > scoreYou) { emoji = '🍂 Reflection'; title = 'Quiet Insight'; }
    else { emoji = '☯️ Harmony'; title = 'Balanced Spirit'; }

    document.getElementById('result-emoji').innerHTML = emoji;
    document.getElementById('result-title').innerText = title;
    document.getElementById('result-score').innerHTML = `Score: ${sessionScore}`;
    let diffName = difficulty === 'easy' ? 'Easy' : (difficulty === 'medium' ? 'Medium' : 'Hard');
    document.getElementById('result-detail').innerHTML = `You ${scoreYou} : ${scoreBot} AI · Ties ${scoreTie} · ${diffName}`;
    document.getElementById('result-overlay').style.display = 'flex';

    await submitScore(sessionScore, elapsed);
    document.getElementById('round-result-overlay').style.display = 'none';
}


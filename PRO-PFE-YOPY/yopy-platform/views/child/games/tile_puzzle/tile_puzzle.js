function renderBoard() {
    const board = document.getElementById('puzzle-board');
    board.innerHTML = '';

    const maxBoard = Math.min(360, window.innerWidth - 32);
    tileSize = Math.floor((maxBoard - (n-1)*6 - 20) / n);

    board.style.gridTemplateColumns = `repeat(${n}, ${tileSize}px)`;

    tiles.forEach((val, idx) => {
        const tile = document.createElement('div');
        tile.className = 'tile' + (val === 0 ? ' empty' : '');
        tile.style.width  = tileSize + 'px';
        tile.style.height = tileSize + 'px';
        tile.style.fontSize = Math.floor(tileSize * 0.38) + 'px';

        if (val !== 0) {
            tile.style.background = TILE_COLORS[(val-1) % TILE_COLORS.length];
            tile.style.boxShadow  = '0 4px 12px rgba(0,0,0,.18)';

            const num = document.createElement('div');
            num.className = 'tile-num';
            num.textContent = val;
            tile.appendChild(num);

            // Multi-step hint badges
            const hintStep = hintSequence.findIndex(s => s.tileVal === val);
            if (hintStep >= 0) {
                tile.classList.add(`hint-tile-${hintStep+1}`);
                const badge = document.createElement('div');
                badge.className = 'hint-step-badge';
                badge.textContent = hintStep + 1;
                tile.appendChild(badge);
            }

            // ✅ Highlight movable tiles (adjacent to blank)
            const row  = Math.floor(idx / n);
            const col  = idx % n;
            const bRow = Math.floor(blankIdx / n);
            const bCol = blankIdx % n;
            const isAdjacent =
                (row === bRow && Math.abs(col - bCol) === 1) ||
                (col === bCol && Math.abs(row - bRow) === 1);
            if (isAdjacent) {
                tile.classList.add('movable');
            }

            tile.addEventListener('click', () => clickTile(idx));
        }

        board.appendChild(tile);
    });
}

// ════════════════════════════════════════════════════
// TILE MOVEMENT
// ════════════════════════════════════════════════════
function clickTile(idx) {
    if (gameState !== STATE.PLAYING) return;

    const row  = Math.floor(idx / n);
    const col  = idx % n;
    const bRow = Math.floor(blankIdx / n);
    const bCol = blankIdx % n;

    // ✅ Only allow direct neighbors (fixes ALL bugs)
    const isAdjacent =
        (row === bRow && Math.abs(col - bCol) === 1) ||
        (col === bCol && Math.abs(row - bRow) === 1);

    if (!isAdjacent) return;

    // Save state for undo
    moveHistory.push({ tiles: [...tiles], blankIdx });

    // ✅ Simple swap
    [tiles[idx], tiles[blankIdx]] = [tiles[blankIdx], tiles[idx]];
    blankIdx = idx;

    moves++;
    if (collector) collector.record('matches', 1);

    clearHintHighlight();
    hintTileIdx = -1;

    renderBoard();
    updateHUD();
    checkWin();
}

// ── Undo ──
function undoMove() {
    if (gameState !== STATE.PLAYING || moveHistory.length === 0) return;
    const prev = moveHistory.pop();
    tiles    = prev.tiles;
    blankIdx = prev.blankIdx;
    moves    = Math.max(0, moves - 1);
    clearHintHighlight();
    hintTileIdx = -1;
    renderBoard();
    updateHUD();
}

// ── Keyboard support ──
window.addEventListener('keydown', e => {
    if (gameState !== STATE.PLAYING) return;
    const bRow = Math.floor(blankIdx / n);
    const bCol = blankIdx % n;
    let targetIdx = -1;
    switch(e.key) {
        case 'ArrowUp':    if (bRow < n-1) targetIdx = (bRow+1)*n + bCol; break;
        case 'ArrowDown':  if (bRow > 0)   targetIdx = (bRow-1)*n + bCol; break;
        case 'ArrowLeft':  if (bCol < n-1) targetIdx = bRow*n + bCol+1;   break;
        case 'ArrowRight': if (bCol > 0)   targetIdx = bRow*n + bCol-1;   break;
    }
    if (targetIdx >= 0) { e.preventDefault(); clickTile(targetIdx); }
});


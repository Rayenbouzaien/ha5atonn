// ── Timer ──
function startTimer() {
    stopTimer();
    elapsed = 0; startTimestamp = Date.now();
    timerInterval = setInterval(() => {
        elapsed = Math.floor((Date.now()-startTimestamp)/1000);
        document.getElementById('hud-timer').textContent = elapsed+'s';
        const pill = document.getElementById('timer-pill');
        if (elapsed >= 120) pill.classList.add('urgent');
        else pill.classList.remove('urgent');
        updateHUD();
    }, 500);
}
function stopTimer() { clearInterval(timerInterval); }

// ── Submit ──
async function submitScore(points, completionTime) {
    const badge = document.getElementById('submitting-badge');
    badge.classList.add('show');
    try {
        await fetch(`../../../games/tile_puzzle/tile_puzzle_backend.php?action=score`, {
            method:'POST', headers:{'Content-Type':'application/json'},
            body: JSON.stringify({session_id:serverSessionId, points, completion_time:completionTime, difficulty})
        });
    } catch(e) {}
    finally { badge.classList.remove('show'); }
}


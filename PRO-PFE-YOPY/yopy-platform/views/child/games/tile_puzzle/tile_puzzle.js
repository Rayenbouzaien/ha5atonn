// Count inversions in the tile array (ignoring blank)
function countInversions(arr) {
    const a = arr.filter(v => v !== 0);
    let inv = 0;
    for (let i = 0; i < a.length; i++)
        for (let j = i+1; j < a.length; j++)
            if (a[i] > a[j]) inv++;
    return inv;
}

// Check if a puzzle state is solvable
function isSolvable(arr, size) {
    const inv = countInversions(arr);
    if (size % 2 === 1) {
        // Odd grid: solvable iff inversions is even
        return inv % 2 === 0;
    } else {
        // Even grid: solvable iff (inversions + blank_row_from_bottom) is odd
        const blankPos    = arr.indexOf(0);
        const blankRow    = Math.floor(blankPos / size);
        const rowFromBot  = size - blankRow;
        return (inv + rowFromBot) % 2 === 1;
    }
}

// Generate a solved state and shuffle via valid random moves
function generatePuzzle(size, shuffleMoves) {
    // Solved: [1,2,3,...,n*n-1,0]
    let arr = Array.from({length: size*size}, (_,i) => i === size*size-1 ? 0 : i+1);
    let blank = size*size - 1;

    // Shuffle by making random valid moves (always solvable by construction)
    const dirs = [-1, 1, -size, size];
    for (let s = 0; s < shuffleMoves; s++) {
        const valid = dirs.filter(d => {
            const nb = blank + d;
            if (nb < 0 || nb >= size*size) return false;
            if (d ===  1 && blank % size === size-1) return false;
            if (d === -1 && blank % size === 0)      return false;
            return true;
        });
        const d = valid[Math.floor(Math.random() * valid.length)];
        const nb = blank + d;
        [arr[blank], arr[nb]] = [arr[nb], arr[blank]];
        blank = nb;
    }
    return arr;
}

// ════════════════════════════════════════════════════
// A* ALGORITHM — Optimal next move hint
// Heuristic: Manhattan Distance
// Searches up to MAX_NODES nodes before falling back
// to greedy (pure Manhattan distance direction).
// ════════════════════════════════════════════════════

const ASTAR_MAX_NODES = 8000; // safety limit for large grids

function manhattanDistance(arr, size) {
    let dist = 0;
    for (let i = 0; i < arr.length; i++) {
        const v = arr[i];
        if (v === 0) continue;
        const goalIdx = v - 1; // tile v should be at index v-1
        const curRow  = Math.floor(i       / size);
        const curCol  = i       % size;
        const goalRow = Math.floor(goalIdx / size);
        const goalCol = goalIdx % size;
        dist += Math.abs(curRow - goalRow) + Math.abs(curCol - goalCol);
    }
    return dist;
}

// Returns array of up to maxSteps optimal moves: [{tileVal}, ...]
// tileVal is the NUMBER on the tile to move at each step.
function astarNextMoves(startArr, size, maxSteps = 3) {
    const goal = Array.from({length: size*size}, (_,i) => i === size*size-1 ? 0 : i+1).join(',');

    const startKey = startArr.join(',');
    if (startKey === goal) return null; // already solved

    const startBlank = startArr.indexOf(0);
    const startH     = manhattanDistance(startArr, size);
    const open       = [{ arr: startArr, blank: startBlank, g: 0, h: startH, parent: null, movedTileVal: null }];
    const closed     = new Set([startKey]);
    let   explored   = 0;

    const dirs = [ -size, size, -1, 1 ];

    while (open.length > 0 && explored < ASTAR_MAX_NODES) {
        open.sort((a,b) => (a.g+a.h) - (b.g+b.h));
        const cur = open.shift();
        explored++;

        if (cur.arr.join(',') === goal) {
            // Trace back full path as array of tile values
            const path = [];
            let node = cur;
            while (node.parent !== null) {
                path.unshift({ tileVal: node.movedTileVal });
                node = node.parent;
            }
            return path.slice(0, maxSteps);
        }

        for (const d of dirs) {
            const nb = cur.blank + d;
            if (nb < 0 || nb >= size*size) continue;
            if (d === -1 && cur.blank % size === 0)      continue;
            if (d ===  1 && cur.blank % size === size-1) continue;

            const newArr = [...cur.arr];
            const movedTileVal = cur.arr[nb]; // tile at nb slides into blank
            [newArr[cur.blank], newArr[nb]] = [newArr[nb], newArr[cur.blank]];
            const key = newArr.join(',');
            if (closed.has(key)) continue;
            closed.add(key);

            open.push({ arr: newArr, blank: nb, g: cur.g+1, h: manhattanDistance(newArr, size), parent: cur, movedTileVal });
        }
    }

    // Fallback greedy — 1 step
    let bestTile = -1, bestGain = -Infinity;
    const curBlank = startArr.indexOf(0);
    for (const d of dirs) {
        const nb = curBlank + d;
        if (nb < 0 || nb >= size*size) continue;
        if (d === -1 && curBlank % size === 0)      continue;
        if (d ===  1 && curBlank % size === size-1) continue;
        const newArr = [...startArr];
        [newArr[curBlank], newArr[nb]] = [newArr[nb], newArr[curBlank]];
        const gain = startH - manhattanDistance(newArr, size);
        if (gain > bestGain) { bestGain = gain; bestTile = nb; }
    }
    return bestTile >= 0 ? [{ tileVal: startArr[bestTile] }] : null;
}


async function requestHint() {
    if (gameState !== STATE.PLAYING || astarWorking) return;

    clearHintHighlight();
    astarWorking = true;
    document.getElementById('hint-btn').disabled  = true;
    document.getElementById('astar-status').textContent = '🔍 A* searching for optimal moves…';
    document.getElementById('hint-msg').textContent = '';

    await new Promise(r => setTimeout(r, 30));

    const result = astarNextMoves(tiles, n, 3);

    astarWorking = false;
    document.getElementById('hint-btn').disabled  = false;
    document.getElementById('astar-status').textContent = '';

    if (!result || result.length === 0) {
        document.getElementById('hint-msg').textContent = '✅ Puzzle already solved!';
        return;
    }

    hintsUsed++;
    hintSequence = result;

    renderBoard(); // apply badges

    const steps = result.map((s,i) => `<b>${i+1}→${s.tileVal}</b>`).join(' · ');
    document.getElementById('hint-msg').innerHTML =
        `💡 Next ${result.length} moves: ${steps} — score ×0.85 per hint`;

    clearTimeout(hintClearTimer);
    hintClearTimer = setTimeout(() => {
        clearHintHighlight();
        document.getElementById('hint-msg').textContent = '';
    }, 6000);
}

function clearHintHighlight() {
    clearTimeout(hintClearTimer);
    hintSequence = [];
    document.querySelectorAll('.hint-tile-1,.hint-tile-2,.hint-tile-3').forEach(t => {
        t.classList.remove('hint-tile-1','hint-tile-2','hint-tile-3');
        t.querySelectorAll('.hint-step-badge').forEach(b => b.remove());
    });
}

// ════════════════════════════════════════════════════
// WIN CHECK & END GAME
// ════════════════════════════════════════════════════

function checkWin() {
    for (let i = 0; i < tiles.length - 1; i++) {
        if (tiles[i] !== i + 1) return;
    }
    if (tiles[tiles.length - 1] !== 0) return;
    // Solved!
    setTimeout(() => endGame(), 200);
}

async function endGame() {
    if (gameState !== STATE.PLAYING) return;
    gameState = STATE.DONE;
    stopTimer();
    clearHintHighlight();
    if (collector) await collector.flush();

    const score = calcScore();
    const cfg   = DIFF_CONFIG[difficulty];

    let emoji;
    if (score > 800) emoji = '🏆';
    else if (score > 400) emoji = '🎉';
    else if (score > 100) emoji = '👍';
    else emoji = '😅';

    document.getElementById('result-emoji').textContent        = emoji;
    document.getElementById('result-title').textContent        = 'Solved!';
    document.getElementById('result-score-display').textContent = `Score: ${score}`;
    document.getElementById('result-detail').textContent =
        `${elapsed}s · ${moves} moves · ${hintsUsed} hint${hintsUsed!==1?'s':''} · ${cfg.label}`;
    document.getElementById('result-overlay').classList.add('show');

    await submitScore(score, Math.max(3, elapsed));
    gameState = STATE.SAVED;
}

function calcScore() {
    const cfg         = DIFF_CONFIG[difficulty];
    const timeBonus   = Math.max(0, 1000 - elapsed * 2);
    const movePenalty = Math.max(0, (moves - n*n*2) * 3);
    const raw         = Math.max(0, Math.round((timeBonus - movePenalty) * cfg.mult / 10));
    // Each hint halves the remaining score
    return Math.round(raw * Math.pow(0.85, hintsUsed));
}


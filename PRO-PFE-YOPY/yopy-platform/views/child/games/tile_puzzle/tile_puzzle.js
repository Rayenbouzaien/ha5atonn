// ── Particle canvas ──
(function particles() {
    const c = document.getElementById('particles');
    const x = c.getContext('2d');
    const COLORS = ['#FFB0D0','#FFD700','#4AACFF','#4ECDC4','#9D3FFF'];
    let pts = [];
    function resize() { c.width=window.innerWidth; c.height=window.innerHeight; }
    resize(); window.addEventListener('resize', resize);
    for (let i=0;i<40;i++) pts.push({
        px:Math.random()*c.width, py:Math.random()*c.height,
        r:Math.random()*3+1, vx:(Math.random()-.5)*.4, vy:(Math.random()-.5)*.4,
        col:COLORS[Math.floor(Math.random()*COLORS.length)],
    });
    function draw() {
        x.clearRect(0,0,c.width,c.height);
        pts.forEach(p=>{
            x.beginPath(); x.arc(p.px,p.py,p.r,0,Math.PI*2);
            x.fillStyle=p.col+'55'; x.fill();
            p.px+=p.vx; p.py+=p.vy;
            if(p.px<0||p.px>c.width)  p.vx*=-1;
            if(p.py<0||p.py>c.height) p.vy*=-1;
        });
        requestAnimationFrame(draw);
    }
    if (!window.matchMedia('(prefers-reduced-motion: reduce)').matches) draw();
})();